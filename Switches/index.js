var grade = "F";

if(grade == "A"){
    console.log("You did great!");
}
else if(grade == "B"){
    console.log("You did good!")
}
else if(grade == "C"){
    console.log("You did okay")
}
else if(grade == "D"){
    console.log("You... did not do that well")
}
else if(grade == "F"){
    document.write("YOU FAILED!")
}